/// Unit conversion constants used across the application.
class UnitConversions {
  UnitConversions._();

  /// Conversion factor from kilometers to miles.
  static const double kmToMiles = 0.621371;

  /// Conversion factor from miles to kilometers.
  static const double milesToKm = 1.609344;

  /// Conversion factor from meters to feet.
  static const double metersToFeet = 3.28084;

  /// Conversion factor from feet to meters.
  static const double feetToMeters = 0.3048;
}
